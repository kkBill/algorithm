/**
 * Definition for a Node.
 * type Node struct {
 *     Val int
 *     Children []*Node
 * }
 */

func postorder(root *Node) []int {
    var res []int
    if root == nil {
    	return res
	}

	var helper func(node *Node)
    helper = func(node *Node) {
    	if node != nil {
			for _, child := range node.Children {
				helper(child)
			}
			res = append(res, node.Val)
		}
	}
	helper(root)
	return res
}
